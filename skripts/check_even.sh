#!/bin/bash

read -p "Введите целое число: " num

# Проверяем, является ли ввод числом (опционально, но полезно)
if ! [[ "$num" =~ ^-?[0-9]+$ ]]; then
    echo "Ошибка: введите корректное целое число."
    exit 1
fi

# Проверка чётности с помощью оператора остатка от деления (%)
if [ $((num % 2)) -eq 0 ]; then
    echo "$num — чётное число."
else
    echo "$num — нечётное число."
fi