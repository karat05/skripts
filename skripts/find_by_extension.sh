#!/bin/bash

# Запрашиваем расширение (без точки или с ней — обработаем оба случая)
read -p "Введите расширение файла (например: txt, .log, js): " ext

# Убираем точку в начале, если она есть
ext="${ext#.}"

# Проверяем, что расширение не пустое
if [ -z "$ext" ]; then
    echo "Ошибка: расширение не указано."
    exit 1
fi

# Ищем файлы с этим расширением в текущей директории
echo "Поиск файлов с расширением .$ext в текущей папке..."

# Счётчик найденных файлов
count=0

# Цикл по всем файлам с данным расширением
for file in *."$ext"; do
    # Проверяем, что файл существует (на случай, если ничего не найдено)
    if [ -f "$file" ]; then
        echo "  - $file"
        ((count++))
    fi
done

# Вывод результата
if [ $count -eq 0 ]; then
    echo "Файлы с расширением .$ext не найдены."
else
    echo "Найдено файлов: $count"
fi