#!/bin/bash

# Запрашиваем путь к файлу
read -p "Введите путь к файлу: " filepath

# Проверяем, что путь не пустой
if [ -z "$filepath" ]; then
    echo "Ошибка: путь не указан."
    exit 1
fi

# Преобразуем путь из Windows-стиля (если нужно) — Git Bash обычно сам справляется
# Но лучше использовать прямой путь в стиле /c/...

# Проверяем, существует ли файл
if [ ! -f "$filepath" ]; then
    echo "Ошибка: файл '$filepath' не найден или не является обычным файлом."
    exit 1
fi

# Считаем строки с помощью wc -l
lines=$(wc -l < "$filepath")

# Выводим результат
echo "Количество строк в файле '$filepath': $lines"